HRESULT tileFloor::init()
{
	//샘플타일 초기화
	_tileSample = new tile;
	_tileSample->tileSetup("맵툴타일바닥", 740, 100, ATTR_NONE, TYPE_TERRAIN, 1.5);

	//선택할 샘플타일 담을 구조체
	ZeroMemory(&_selectTile, sizeof(tagTile));

	return S_OK;
}

